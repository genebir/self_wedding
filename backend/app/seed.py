def run_seed() -> None:
    db = SessionLocal()
    try:
        if db.scalar(select(Taxonomy.id).limit(1)):
            return  # 이미 시드됨
        for i, (slug, name, children) in enumerate(TAXONOMY):
            parent = Taxonomy(slug=slug, name=name, sort_order=i)
            db.add(parent)
            db.flush()
            for j, (cslug, cname) in enumerate(children):
                db.add(Taxonomy(slug=cslug, name=cname, parent_id=parent.id, sort_order=j))
        for i, (slug, name, cat, summary, detail, note, ask) in enumerate(GLOSSARY):
            db.add(
                FeeGlossary(
                    slug=slug, name=name, category_slug=cat, summary=summary,
                    detail=detail, typical_note=note, ask_vendor=ask, sort_order=i,
                )
            )
        for i, (title, desc, cat, offset) in enumerate(CHECKLIST):
            db.add(
                ChecklistTemplate(
                    title=title, description=desc, category_slug=cat,
                    offset_days=offset, sort_order=i,
                )
            )
        db.commit()
    finally:
        db.close()
